  ios_base::sync_with_stdio(0);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int x, n;
    cin >> x >> n;
    int d = x / n, ans = 1;
    for (ll i = 1; i * i <= (ll)x; i++)
      if (x % i == 0) {
        int d1 = i, d2 = x / i;
        if (d1 <= d) ans = max(ans, d1);
        if (d2 <= d) ans = max(ans, d2);
      }
    cout << ans << "\n";
  }
  return 0;
